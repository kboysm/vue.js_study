for (let i = 0; i < productName.length; i++) {
    let descript;
    let category;
    let productImg = `http://localhost:3000/images/${i}.jpg`
    let bestProduct = i % 4 == 0 ? true : false

    if (i <= 10) {
        descript = descriptionfrog(productName[i])
        category = 'frog'
    } else if (i > 10 && i <= 27) {
        descript = descriptionlizard(productName[i])
        category = 'lizard'
    } else if (i > 27 && i <= 35) {
        descript = descriptionsnake(productName[i])
        category = 'snake'
    } else if (i > 35 && i <= 50) {
        descript = descriptionturtle(productName[i])
        category = 'turtle'
    } else if (i > 50 && i <= 60) {
        descript = descriptionscorpion(productName[i])
        category = 'scorpion'
    }
    const product = new Product(productName[i], descript, category, productImg, 'admin', bestProduct, productPrice[i])
    productList.push(product)
}
